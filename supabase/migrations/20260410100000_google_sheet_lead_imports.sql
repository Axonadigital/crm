CREATE TABLE IF NOT EXISTS lead_import_sources (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  source_type text NOT NULL DEFAULT 'google_sheet_csv',
  sheet_url text NOT NULL,
  sheet_gid text,
  is_active boolean NOT NULL DEFAULT false,
  batch_size_default integer NOT NULL DEFAULT 50,
  last_imported_row integer NOT NULL DEFAULT 1,
  last_successful_run_at timestamp with time zone,
  last_run_status text NOT NULL DEFAULT 'idle',
  last_run_message text,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  updated_at timestamp with time zone NOT NULL DEFAULT now(),

  CONSTRAINT chk_lead_import_sources_source_type
    CHECK (source_type IN ('google_sheet_csv')),
  CONSTRAINT chk_lead_import_sources_batch_size
    CHECK (batch_size_default > 0),
  CONSTRAINT chk_lead_import_sources_last_imported_row
    CHECK (last_imported_row >= 1),
  CONSTRAINT chk_lead_import_sources_last_run_status
    CHECK (last_run_status IN ('idle', 'running', 'success', 'partial', 'failed'))
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_lead_import_sources_single_active
  ON lead_import_sources ((is_active))
  WHERE is_active = true;

CREATE INDEX IF NOT EXISTS idx_lead_import_sources_active
  ON lead_import_sources(is_active);

CREATE OR REPLACE FUNCTION update_lead_import_sources_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path TO '';

CREATE OR REPLACE FUNCTION claim_lead_import_source(
  p_source_id bigint,
  p_stale_before timestamp with time zone
)
RETURNS SETOF lead_import_sources
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
BEGIN
  RETURN QUERY
  UPDATE public.lead_import_sources
  SET
    last_run_status = 'running',
    last_run_message = null,
    updated_at = now()
  WHERE id = p_source_id
    AND (
      last_run_status <> 'running'
      OR updated_at < p_stale_before
    )
  RETURNING *;
END;
$$;

DROP TRIGGER IF EXISTS lead_import_sources_updated_at_trigger ON lead_import_sources;
CREATE TRIGGER lead_import_sources_updated_at_trigger
  BEFORE UPDATE ON lead_import_sources
  FOR EACH ROW
  EXECUTE FUNCTION update_lead_import_sources_updated_at();

ALTER TABLE lead_import_sources ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can view lead import sources" ON lead_import_sources
  FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Users can insert lead import sources" ON lead_import_sources
  FOR INSERT WITH CHECK (auth.role() = 'authenticated');

CREATE POLICY "Users can update lead import sources" ON lead_import_sources
  FOR UPDATE USING (auth.role() = 'authenticated');

CREATE POLICY "Users can delete lead import sources" ON lead_import_sources
  FOR DELETE USING (
    EXISTS (
      SELECT 1
      FROM public.sales
      WHERE user_id = auth.uid()
        AND administrator = true
    )
  );

