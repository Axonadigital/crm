  rows_inserted integer NOT NULL DEFAULT 0,
  rows_skipped_duplicates integer NOT NULL DEFAULT 0,
  rows_failed integer NOT NULL DEFAULT 0,
  status text NOT NULL DEFAULT 'running',
  error_summary text,
  imported_company_ids jsonb NOT NULL DEFAULT '[]'::jsonb,

  CONSTRAINT chk_lead_import_runs_triggered_by
    CHECK (triggered_by IN ('manual', 'scheduled')),
  CONSTRAINT chk_lead_import_runs_requested_batch_size
    CHECK (requested_batch_size > 0),
  CONSTRAINT chk_lead_import_runs_status
    CHECK (status IN ('running', 'success', 'partial', 'failed'))
);

CREATE INDEX IF NOT EXISTS idx_lead_import_runs_source_id
  ON lead_import_runs(source_id);

CREATE INDEX IF NOT EXISTS idx_lead_import_runs_started_at
