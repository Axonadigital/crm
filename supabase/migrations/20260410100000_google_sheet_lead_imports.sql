ALTER TABLE companies
  ADD COLUMN IF NOT EXISTS import_source_id bigint REFERENCES lead_import_sources(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS import_run_id bigint REFERENCES lead_import_runs(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS source_row_number integer,
  ADD COLUMN IF NOT EXISTS processing_order integer,
  ADD COLUMN IF NOT EXISTS prospecting_status text;

DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.table_constraints
    WHERE constraint_name = 'chk_companies_prospecting_status'
      AND table_name = 'companies'
  ) THEN
    ALTER TABLE companies DROP CONSTRAINT chk_companies_prospecting_status;
  END IF;

  ALTER TABLE companies
    ADD CONSTRAINT chk_companies_prospecting_status
    CHECK (
      prospecting_status IS NULL OR prospecting_status IN (
        'imported',
        'enriching',
        'call_ready',
        'needs_review',
        'completed',
        'disqualified'
      )
    );
END $$;

CREATE INDEX IF NOT EXISTS idx_companies_import_source_id
  ON companies(import_source_id);

CREATE INDEX IF NOT EXISTS idx_companies_import_run_id
  ON companies(import_run_id);

CREATE INDEX IF NOT EXISTS idx_companies_processing_order
