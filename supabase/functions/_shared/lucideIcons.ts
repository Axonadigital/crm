/** Get an inline SVG icon by name, with optional size override */
export function getIcon(
  name: string,
  size: number = 24,
  color: string = "currentColor",
): string {
  const svg = lucideIcons[name] || lucideIcons["check"];
  if (size === 24 && color === "currentColor") return svg;
  return svg
    .replace('width="24"', `width="${size}"`)
    .replace('height="24"', `height="${size}"`)
    .replace('stroke="currentColor"', `stroke="${color}"`);
}
